/* 10 - Dado un array que contiene ["azul", "amarillo", "rojo", "verde", "rosa"] determinar si un color introducido por el usuario
a trav√©s de un prompt se encuentra dentro del array o no. */

let array=["azul","amarillo","rojo","verde","rosa"];
let color=(prompt("ingrese un color para ver si esta dentro del arreglo").toLowerCase());
let indica=false;
let posicion=0;

for (i=0;i<array.length;i++){
    if(array[i]==color){
        indica=true;
        posicion=i;
    }}
if(indica==true){
    document.write(`${color} esta dentro del arreglo en la posicion ${posicion}`);
}
else{
    document.write(`${color} no forma parte del arreglo`);
}
